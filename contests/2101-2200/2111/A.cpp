int solve() {
  ll x;
  cin >> x;

  ll now[3] = {0, 0, 0};
  int ans = 0;
  while (now[0] < x) {
    ans++;
    now[0] = min(x, now[1] * 2 + 1);
    sort(now, now + 3);
  }

  return ans;
}

